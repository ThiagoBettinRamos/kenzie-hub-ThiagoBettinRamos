export const treatModule = (num) => {
    if (num === "1") return "Primeiro módulo"
    if (num === "2") return "Segundo módulo"
    if (num === "3") return "Terceiro módulo"
    if (num === "4") return "Quarto módulo"
    if (num === "5") return "Quinto módulo"
    if (num === "6") return "sexto módulo"
    return "Módulo não selecionado"
}